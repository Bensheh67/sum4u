# ================================================================
# 截图增强模板
# ================================================================
def prompt_with_screenshots(base_prompt: str, keyframes: List[Dict], video_duration: float = None) -> str:
    """
    在基础提示词上添加截图引用指导

    Args:
        base_prompt: 基础提示词
        keyframes: 关键帧列表，每项包含 timestamp, reason, relevance, filename
        video_duration: 视频总时长（秒），用于生成备用截图位置
    """
    if keyframes:
        # 有截图可用，正常添加截图引用
        frame_lines = ["\n【可用截图（必须插入到总结中，不要遗漏）】"]
        for i, kf in enumerate(keyframes):
            ts = kf["timestamp"]
            reason = kf.get("reason", "关键帧")
            filename = kf.get("filename", f"frame_{i+1:03d}.jpg")
            h = int(ts // 3600)
            m = int((ts % 3600) // 60)
            s = int(ts % 60)
            time_str = f"{h:02d}:{m:02d}:{s:02d}"
            frame_lines.append(f"- [{time_str}] {reason} → ![{reason}](screenshots/{filename})")

        frame_info = "\n".join(frame_lines)

        screenshot_addition = f"""
{frame_info}

【重要规则】
1. 必须在每个主要章节（## 标题）下插入至少一张相关截图
2. 截图引用格式：![描述](screenshots/文件名)
3. 每个截图只能使用一次，不要重复引用
4. 只引用上面列表中提供的截图
"""
    else:
        # 没有截图可用，但仍然要求 AI 生成结构化总结
        # 添加截图占位符指导，让 AI 在合适位置预留截图空间
        screenshot_addition = """
【截图说明】
本次处理未提取到视频截图，总结将以纯文本形式呈现。
"""
        # 如果提供了视频时长，生成一些参考时间点
        if video_duration:
            h = int(video_duration // 3600)
            m = int((video_duration % 3600) // 60)
            s = int(video_duration % 60)
            screenshot_addition += f"""
视频总时长: {h:02d}:{m:02d}:{s:02d}，可在以下位置添加截图：
- 开头介绍部分
- 核心内容展开部分
- 总结收尾部分
"""

    return base_prompt + screenshot_addition
